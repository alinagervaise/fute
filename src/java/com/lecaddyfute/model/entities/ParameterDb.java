package com.lecaddyfute.model.entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Z
 */
@Entity
@Table(name = "parameter_db")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "ParameterDb.findAll", query = "SELECT p FROM ParameterDb p"),
    @NamedQuery(name = "ParameterDb.findById", query = "SELECT p FROM ParameterDb p WHERE p.id = :id"),
    @NamedQuery(name = "ParameterDb.findByName", query = "SELECT p FROM ParameterDb p WHERE p.name = :name"),
    @NamedQuery(name = "ParameterDb.findByValue", query = "SELECT p FROM ParameterDb p WHERE p.value = :value")})
public class ParameterDb implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 50)
    @Column(name = "name")
    private String name;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 50)
    @Column(name = "value")
    private String value;

    public ParameterDb() {
    }

    public ParameterDb(Integer id) {
        this.id = id;
    }

    public ParameterDb(Integer id, String name, String value) {
        this.id = id;
        this.name = name;
        this.value = value;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ParameterDb)) {
            return false;
        }
        ParameterDb other = (ParameterDb) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.lecaddyfute.model.entities.ParameterDb[ id=" + id + " ]";
    }
    
}
